<h1>Адам Мицкевич</h1>
<p>
<h3>Городская зима</h3>
Прошли дожди весны, удушье лета,
<br>И осени окончился потоп,
<br>И мостовой, в холодный плащ одетой,
<br>Не режет сталь блестящих фризских стоп.
<br>Держала осень в заточенье дома.
<br>На вольный воздух выйдем, на мороз!
<br>Кареты лондонской не слышно грома,
<br>И не раздавит нас металл колес…
<br>Приветствуй горожан, пора благая!
<br>И неманцев и ляхов одарят,
<br>Сердца их для надежды раскрывая,
<br>Улыбки тысяч фавнов и дриад.
<br>Все радует, бодрит и восхищает!
<br>Пью воздуха холодную струю,
<br>Которая дыханье очищает,
<br>Или на хлопья снежные смотрю.
<br>Одна снежинка плавает в стихии,
<br>Другая – та, что тяжелей, – легла.
<br>А эти улетят в поля сухие.
<br>Вилийские побелят зеркала.
<br>Но кто в селе глядит, как заключенный,
<br>На лысый холм, на одичавший дол
<br>И на деревья рощи обнаженной,
<br>Ветвям которых снегопад тяжел,
<br>Тот, опечален небом, ставшим серым,
<br>Бросает край уныния и льда
<br>И, променяв на Плутоса Цереру,
<br>В карете с золотом летит сюда.
<br>Пред ним – гостеприимные ворота.
<br>Дом краской и резьбою веселит.
<br>Он забывает сельские заботы
<br>В кругу очаровательных харит.
<br>В селе, едва редеет мгла ночная,
<br>Церера сразу встать неволит нас.
<br>Здесь – солнце жжет, зенита достигая,
<br>А я лежу, не размыкая глаз.
<br>Потом в нанкине, наскоро надетом,
<br>Я, модной молодежи круг созвав,
<br>Болтаю с ними, – и за туалетом
<br>Проходит утро, полное забав.
<br>Один в трюмо себя обозревает,
<br>Бальзам на кудри золотые льет;
<br>Другой стамбульский горький дым вдыхает
<br>Или настой травы китайской пьет.
<br>Но вот уже двенадцать бьет! Скорее
<br>На улицу – и я уже в санях.
<br>И росомаха или соболь, грея,
<br>Игольчатые на моих плечах.
<br>Я в зал вхожу, где, восхищая взоры,
<br>Стол пиршества для избранных накрыт.
<br>Напитков вкусных, здесь полны фарфоры,
<br>И яства разжигают аппетит.
<br>Коньяк и пунши в хрустале граненом,
<br>Столетний зной венгерского вина;
<br>Мускат по вкусу дамам восхищенным:
<br>Он веселит, однако мысль – ясна.
<br>Блестят глаза, а чаши вновь налиты…
<br>Остроты, шутки, пылкие слова…
<br>Не у одной из дам горят ланиты,
<br>В огне от нежных взглядов голова.
<br>Но вот и солнце никнет. Сумрак синий
<br>Таит благодеяния зимы.
<br>Сигнал разъезда дали нам богини.
<br>И лестницы гремят. Уходим мы.
<br>Тот, кто слепому счастью доверяет,
<br>Вступает, фараон, в твою страну
<br>Или искусно кием управляет
<br>Слонов точеных гонит по сукну.
<br>Когда же ночь раздвинет мрак тяжелый
<br>И в окнах вспыхнет множество огней,
<br>Кончает молодежь свой день веселый,
<br>Шлифуя снег полозьями саней.

