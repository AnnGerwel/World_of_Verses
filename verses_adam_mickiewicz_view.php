<h3>Песнь</h3>
Пусть счастьем глаза загорятся,
<br>Чело нам украсит венок,
<br>Обнимемся все мы по-братски,
<br>Сойдемся в веселый кружок!
<br>Пускай к нам не ведают входа
<br>Обманы, предательство, лесть;
<br>Здесь чтится высоко свобода,
<br>Отчизна, наука и честь.
<br>Мы руки друг другу протянем,
<br>Откроем друг другу сердца
<br>И помыслы, чувства, желанья
<br>Поведаем все до конца.
<br>Здесь сгинут страдания тени
<br>Средь песен, утех, развлечений.
<br>Кто стал нашим братом и другом,
<br>В труде, средь веселых забав,
<br>В зеленом венке и за плугом
<br>Пусть помнит всегда наш Устав.
<br>Пусть он вдохновится присягой,
<br>Что здесь согласился принесть,
<br>Всю жизнь защищает с отвагой
<br>Отчизну, науки и честь!
<br>Дойдем мы, хоть трудной дорогой,
<br>До счастья, когда подадут
<br>Все руки друг другу. Помогут
<br>Нам смел ость, согласье и труд!

<br></br>
<h3>Тосты</h3>
Как наша прожила б планета,
<br>Как люди жили бы на ней
<br>Без теплоты, магнита, света
<br>И электрических лучей?
<br>Что было бы? Пришла бы снова
<br>Хаоса мрачная пора.
<br>Лучам – приветственное слово.
<br>А солнцу – громкое ура!
<br>Но что лучи иль искры света,
<br>Когда морозом мир объят
<br>И сердце наше не согрето?
<br>Привет теплу! Теплу виват!
<br>Теплу и свету люди рады,
<br>Но ветер их разъединит,
<br>Не встретив на пути преграды.
<br>Магнит сюда! Ура, магнит!
<br>Теперь мы тесный круг составим
<br>При ярких солнечных лучах
<br>И электричество восславим
<br>С бутылкой лейденской в руках!

