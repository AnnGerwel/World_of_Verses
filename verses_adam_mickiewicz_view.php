<h3>Первоцвет</h3>
С небесной песней самой ранней
<br>Примчался жаворонок звонкий;
<br>Цветочек ранний на поляне
<br>Блеснул под золотистой пленкой.
<br>Я
<br>Цветочек милый, рановато!
<br>Еще морозом полночь веет,
<br>Еще в дубравах сыровато
<br>И плесень на горах белеет.
<br>Прижмурь златые огонечки,
<br>Под матушкин подол укройся,
<br>Зубочков инея побойся,
<br>Страшна роса холодной ночки!
<br>Цветочек
<br>Как мотыльки, родясь с рассветом,
<br>Мы к полдню гибнем. Больше счастья
<br>В одном апрельском миге этом,
<br>Чем в целых декабрях ненастья.
<br>Коль дар богам воздать ты хочешь,
<br>Друзьям своим, своей любимой
<br>Вплети меня ты в свой веночек,
<br>И будет дар незаменимый!
<br>Я
<br>Средь чахлых травок перелеска
<br>Ты вырос, о цветочек милый;
<br>В тебе ни мощи и ни блеска,
<br>Так чем ты мил, цветочек хилый?
<br>Чем? У тюльпана есть корона,
<br>Весь облик лилии – державен,
<br>У розы – расписное лоно,
<br>У зорь – огонь… А ты чем славен?
<br>И почему ты полон все же
<br>Надеждою несокрушимой,
<br>Что будешь ты всего дороже
<br>Моим друзьям, моей любимой?
<br>Цветочек
<br>Твои друзья мне будут рады
<br>Весны посланцу, ангелочку;
<br>Ведь дружбе блеска и не надо,
<br>Ей тень любезна, как цветочку!
<br>Достоин ли я доли этой?
<br>Ах, очи неземной Марыли
<br>За молодости первоцветы
<br>Лишь первой слезкой отдарили!
